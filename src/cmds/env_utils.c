#include "../../include/minishell.h"

/**
 * @brief Frees an array of environment variables
 * @param env The environement array to free
 */
void	free_env_arr(char **env)
{
	size_t	i;

	if (!env)
		return ;
	i = 0;
	while (env[i])
	{
		free(env[i]);
		i++;
	}
	free(env);
}
